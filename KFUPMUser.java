
public class KFUPMUser {
	private String name;
	private String email;
	private String phoneNumber;
	private int ID;

	public KFUPMUser() {}
	
	public KFUPMUser(String name, String email, String phoneNumber, int ID) {
		this.name=name;
		this.email=email;
		this.phoneNumber=phoneNumber;
		this.ID=ID;
	}
}
